"""
Entradas
Edad 1--> int--> Edad1
Edad 2--> int--> Edad2
Edad 3--> int--> Edad3
Salidas
Promedio edades--> int--> m
"""
#Comentario de una sola linea
Edad1=int(input("Ingrese la edad 1: "))
Edad2=int(input("Ingrese la edad 2: "))
Edad3=int(input("Ingrese la edad 3: "))
m=(Edad1+Edad2+Edad3)/3
print("El promedio de las edades es: "+str(m))
